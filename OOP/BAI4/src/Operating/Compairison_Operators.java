package Operating;

public class Compairison_Operators {
    public static void main(String[] args) {
        double a = 10.99;
        int b = 10;
        System.out.println(a==b); // false
        System.out.println(a>=b); // true
        System.out.println(a<=b); // false
        System.out.println(a!=b); // true
        System.out.println(a>b); // true
        System.out.println(a<b); // false
    }
}
