package Bai05;

public class ArrayInLine02 {
    private int a[] = {0,9,8,1,2,6,3,7,4,5};
    //----------------------------------------------------------------------------------------//
    public  ArrayInLine02(){

    }
    //----------------------------------------------------------------------------------------//
    public int[] getA(){
        return a;
    }
    //----------------------------------------------------------------------------------------//
    public void setA(int a[]){
        this.a = a;
    }
    //----------------------------------------------------------------------------------------//
    public void sort(){
        for(int i=0; i<a.length-1; i++){
            for(int j=0; j<a.length-i-1; j++){
                if(a[j] > a[j+1]){
                    int temp = a[j];
                    a[j] = a[j+1];
                    a[j+1] = temp;
                }
            }
        }
        System.out.println("Mang: ");
        for(int i: a) {
            System.out.print(i + " ");
        }
    }
    //----------------------------------------------------------------------------------------//
}
